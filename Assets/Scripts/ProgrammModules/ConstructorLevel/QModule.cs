using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class QModule
{

    public float X;
    public float Y;
    public float Z;
    public float W;

    public QModule()
    {
        X = 0;
        Y = 0;
        Z = 0;
        W = 0;
    }

    public QModule(float x, float y, float z, float w)
    {
        X = x;
        Y = y;
        Z = z;
        W = w;
    }
}

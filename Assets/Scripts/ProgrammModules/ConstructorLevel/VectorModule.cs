using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class VectorModule
{
    public float X;
    public float Y;
    public float Z;

    public VectorModule()
    {
        X = 0;
        Y = 0;
        Z = 0;
    }
    public VectorModule(float x, float y, float z)
    {
        X = x;
        Y = y;
        Z = z;
    }
}
